<p>
	<b>The vision of SOCIETIES</b><br> The vision of SOCIETIES is to
	develop a complete, integrated Community Smart Space (CSS), which
	extends pervasive systems beyond the individual to dynamic communities
	of users. CSSs will embrace on-line community services, such as Social
	Networking in order to offer new and powerful ways of working,
	communicating and socialising. <br> SOCIETIES will radically improve
	the utility and scope of future Internet services by merging social
	computing and pervasive computing through the design, implementation
	and evaluation of an open scalable service architecture and platform
	for self-orchestrating Community Smart Spaces.
</p>
<p>
	<b>The goal of SOCIETIES will be achieved through four key objectives:</b>
	<br> - To facilitate the creation, organisation, management and
	communication of communities via Community Smart Spaces, where
	pervasive computing is integrated with social computing communities;<br>
	- To provide an enhanced user experience for both individuals and
	entire user communities, based on proactive smart space behaviour and
	dynamic sharing of community resources across geographic boundaries;<br>
	- To design and prototype a robust open and scalable system for
	self-orchestrating Community Smart Spaces; <br> - To evaluate, through
	strong involvement of end-users, the usefulness and acceptance of the
	developed CSS software via three user trials with the following groups:
	<br> • Enterprise Users: Enterprise communities play an important role
	in bringing together people, goods and services within global markets,
	local ecosystems or large organisations. The CSS concept will bridge
	the gap between smart IT systems and established enterprise community
	activities. <br> • Students: Students adapt easily to new technology,
	and since communication and social networking play an integral role in
	their lives, they are most likely to adopt CSSs, using them in ways
	both foreseen and unforeseen. <br> • Disaster Relief Experts: The
	ability to rapidly form a disaster management community from all the
	closely located relief teams can help save lives, property, and the
	environment.
</p>

